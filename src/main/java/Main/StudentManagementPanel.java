    public void loadDataTable() {
        try {
            model.setRowCount(0);
            list = new SinhVienDAO().getListStudent();
            for(SinhVien sv : list){
                model.addRow(new Object[]{
                    sv.getMaSinhVien(), sv.getHoTen(), sv.getEmail(), sv.getSoDT(), sv.getGioiTinh()==1?"Nam":"Nữ", sv.getDiaChi()
                });
            }
            model.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.ShowErrorDialog(parent, e.getMessage(), "Lỗi");
        }
    }
    
