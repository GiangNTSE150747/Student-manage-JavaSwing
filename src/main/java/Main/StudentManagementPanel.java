    private void tblTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTableMouseClicked
        int SelectedRow = tblTable.getSelectedRow();
        for (SinhVien s : list) {
            if (s.getMaSinhVien().equals(tblTable.getValueAt(SelectedRow, 0))) {
                txtMaSinhVien.setText(s.getMaSinhVien());
                txtHoTen.setText(s.getHoTen());
                txtEmail.setText(s.getEmail());
                txtSdt.setText(s.getSoDT());
                if(s.getGioiTinh() == 1){
                    rbtnNam.setSelected(true);
                }
                else rbtnNu.setSelected(true);
                txtDiaChi.setText(s.getDiaChi());
                //Chỗ này bổ sung hình
                break;
            }
        }
    }//GEN-LAST:event_tblTableMouseClicked

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validataEmpty(txtMaSinhVien, sb, "Mã sinh viên không được để trống");
        DataValidator.validataEmpty(txtHoTen, sb, "Họ tên sinh viên không được để trống");
        if(sb.length()>0){
            JOptionPane.showMessageDialog(parent, sb.toString(), "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(MessageDialogHelper.ShowConfirmDialog(parent, "Bạn có muốn cập nhật?", "Hỏi") == JOptionPane.NO_OPTION){
            return;
        }
        try {
            SinhVien sv = new SinhVien();
            sv.setMaSinhVien(txtMaSinhVien.getText());
            sv.setHoTen(txtHoTen.getText());
            sv.setEmail(txtEmail.getText());
            sv.setSoDT(txtSdt.getText());
            sv.setDiaChi(txtDiaChi.getText());
            sv.setGioiTinh(rbtnNam.isSelected()?1:0);
            sv.setHinh(null);
            
            if(new SinhVienDAO().update(sv)){
                loadDataTable();
                MessageDialogHelper.ShowMessageDialog(parent, "Cập nhật thành công", "Thông báo");
            }
            else{
                MessageDialogHelper.ShowErrorDialog(parent, "Cập nhật không thành công", "Lỗi");
            }
        } catch (Exception e) {
            e.printStackTrace();
             MessageDialogHelper.ShowErrorDialog(parent, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

