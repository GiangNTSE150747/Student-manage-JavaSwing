    public void loadDataTable() {
        try {
            float avg;
            model.setRowCount(0);
            list = new BangDiemDAO().getListBangDiem();
            for (BangDiem bd : list) {
                avg = (float) Math.ceil(((bd.getTiengAnh()+ bd.getTinHoc()+ bd.getGDTC())/3 * 100)/100);
                model.addRow(new Object[]{
                    bd.getMa(), bd.getMaSinhVien(), bd.getTiengAnh(), bd.getTinHoc(), bd.getGDTC(), avg
                });
            }
            model.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.ShowErrorDialog(parent, e.getMessage(), "Lỗi");
        }
    }

