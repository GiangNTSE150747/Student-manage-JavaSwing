    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validataEmpty(txtMaSinhVien2, sb, "Mã sinh viên không được để trống\n");
        DataValidator.validataEmpty(txtTiengAnh, sb, "Tiếng Anh không được để trống\n");
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(parent, sb.toString(), "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            BangDiem bd = new BangDiem();
            bd.setMaSinhVien(txtMaSinhVien2.getText());
            bd.setTiengAnh(Float.parseFloat(txtTiengAnh.getText()));
            bd.setTinHoc(Float.parseFloat(txtTinHoc.getText()));
            bd.setGDTC(Float.parseFloat(txtGDTC.getText()));
            if (new BangDiemDAO().findByMaSinhVien(bd.getMaSinhVien()) != null) {
                if (MessageDialogHelper.ShowConfirmDialog(parent, "Bạn có muốn cập nhật?", "Hỏi") == JOptionPane.NO_OPTION) {
                    return;
                }
                if (new BangDiemDAO().update(bd)) {
                    loadDataTable();
                    MessageDialogHelper.ShowMessageDialog(parent, "Cập nhật thành công", "Thông báo");
                } else {
                    MessageDialogHelper.ShowErrorDialog(parent, "Cập nhật không thành công", "Lỗi");
                }
            } else {
                if (new BangDiemDAO().insert(bd)) {
                    if (new BangDiemDAO().update(bd)) {
                        loadDataTable();
                        MessageDialogHelper.ShowMessageDialog(parent, "Thêm thành công", "Thông báo");
                    } else {
                        MessageDialogHelper.ShowErrorDialog(parent, "Thêm không thành công", "Lỗi");
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.ShowErrorDialog(parent, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

