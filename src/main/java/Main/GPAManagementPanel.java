    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            if(txtMaSinhVien2.getText().equals("")){
                MessageDialogHelper.ShowErrorDialog(parent, "Mã Sinh viên cần phải nhập", "Lỗi");
                return;
            }
            if(new BangDiemDAO().findByMaSinhVien(txtMaSinhVien2.getText()) == null){
                MessageDialogHelper.ShowMessageDialog(parent, "Không tồn tại sinh viên", "Thông báo");
                return;
            }
            if(MessageDialogHelper.ShowConfirmDialog(parent, "Bạn có muốn xóa điểm của sinh viên", "Hỏi") == JOptionPane.NO_OPTION){
                return;
            }
            if(new BangDiemDAO().deleteByMaSinhVien(txtMaSinhVien2.getText())){
                loadDataTable();
                MessageDialogHelper.ShowMessageDialog(parent, "Xóa thành công", "Thông báo");
            }
            else{
                MessageDialogHelper.ShowErrorDialog(parent, "Xoá không thành công", "Lỗi");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.ShowErrorDialog(parent, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void txtTiengAnhFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtTiengAnhFocusLost
        if(txtTiengAnh.getText().equals("") || txtTinHoc.getText().equals("") || txtGDTC.getText().equals("")){
            return;
        }
        float TA = Float.parseFloat(txtTiengAnh.getText());
        float Tin = Float.parseFloat(txtTinHoc.getText());
        float TD = Float.parseFloat(txtGDTC.getText());
        
        float avg = (TA+TD+Tin)/3;
        String st = String.format("%.2f", avg);
        lblGpa.setText(st);
    }//GEN-LAST:event_txtTiengAnhFocusLost


