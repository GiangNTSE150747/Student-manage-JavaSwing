    private void tblGpaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGpaMouseClicked
        int SelectedRow = tblGpa.getSelectedRow();
        for (BangDiem bd : list) {
            if (bd.getMaSinhVien().equals(tblGpa.getValueAt(SelectedRow, 1))) {
                txtMaSinhVien2.setText(bd.getMaSinhVien());
                txtTiengAnh.setText("" + bd.getTiengAnh());
                txtTinHoc.setText("" + bd.getTinHoc());
                txtGDTC.setText("" + bd.getGDTC());
                txtMaSinhVien2FocusLost(null);
                txtTiengAnhFocusLost(null);
                break;
            }
        }
    }//GEN-LAST:event_tblGpaMouseClicked

